Valor1=int(input("Ingrese su numero: "))
operacion=input("Ingrese su operacion: ")
Valor2=int(input("Ingrese su numero 2: "))

if operacion=="+":
    print("la suma es", Valor1+Valor2)
if operacion=="-":
    print("la resta es", Valor1-Valor2)
if operacion=="*":
    print("la multiplicacion es", Valor1*Valor2)
if operacion=="/":
    print("la division es", Valor1/Valor2)

